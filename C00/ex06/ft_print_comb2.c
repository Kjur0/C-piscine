#include <unistd.h>

void	ft_print_comb2(void)
{
	int		a;
	int		b;
	char	str[5];

	a = 0;
	while (a < 99)
	{
		b = a + 1;
		while (b < 100)
		{
			str[0] = '0' + (a / 10);
			str[1] = '0' + (a % 10);
			str[2] = ' ';
			str[3] = '0' + (b / 10);
			str[4] = '0' + (b % 10);
			write(1, str, 5);
			if (a != 98 || b != 99)
			{
				write(1, ", ", 2);
			}
			b++;
		}
		a++;
	}
}
